# texto1 recebe o valor de "sou do"
texto1 = '"sou do"'

# texto2 recebe o valor "back-end"
# caracter de SCAPE é o "\"
# você pode ESCAPAR aspas duplas utilizando aspas simples
# vale reforçar que isso só vale para aspas duplas, outros caracteres não
texto2 = "\"back-end\""

# a função print, concatena e imprime as duas variaveis na tela
print(texto1, texto2)